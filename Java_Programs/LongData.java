//5.Example on long datatype.
import java.util.*;
class LongData
{
	public static void main(String args[])
	{
		long l1=9223372036854775807L;
		System.out.println("long value"+l1);
		Scanner s=new Scanner(System.in);
		System.out.println("enter a long number :");
		l1=s.nextLong();
		System.out.println("long value :"+l1);
	}

}