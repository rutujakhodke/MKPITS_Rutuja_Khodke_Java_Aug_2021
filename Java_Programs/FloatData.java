//7.Example on double datatype.
import java.util.*;
class FloatData
{
	public static void main(String args[])
	{
		float f1=123.4567f;
		System.out.println("float value"+f1);
		Scanner s=new Scanner(System.in);
		System.out.println("enter a float number :");
		f1=s.nextFloat();
		System.out.println("float value :"+f1);
	}

}