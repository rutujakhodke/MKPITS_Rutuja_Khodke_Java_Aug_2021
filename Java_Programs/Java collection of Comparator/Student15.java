//1.Java 8 Comparator Example.In this example sorting the elements of list on the basis of age and name.
class Student15{
 int rollno;
 String name;
 int age;
 public Student15(int rollno,String name,int age){
  this.rollno=rollno;
  this.name=name;
  this.age=age;
 }
 public int getRollno(){
  return rollno;
 }
 public void setRollno(int rollno){
  this.rollno=rollno;
 }
 public String getName(){
  return name;
 }
 public void setName(String name){
  this.name=name;
 }
 public int getAge(){
  return age;
 }
 public void setAge(int age){
  this.age=age;
 }
}

