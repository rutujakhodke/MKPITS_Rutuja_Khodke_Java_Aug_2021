//6.Example on double datatype.
import java.util.*;
class DoubleData
{
	public static void main(String args[])
	{
		double d1=123.123451234567d;
		System.out.println("double value"+d1);
		Scanner s=new Scanner(System.in);
		System.out.println("enter a double number :");
		d1=s.nextDouble();
		System.out.println("double value :"+d1);
	}
}